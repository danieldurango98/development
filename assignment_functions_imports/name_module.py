# full_name
def full_name(first_name,last_name):
    result = f'{first_name} {last_name}'
    print(result)


# reverse_name
def reverse_name(first_name,last_name):
    result = f'{last_name} {first_name}'
    print(result)


# get_initials
def get_initials(first_name, last_name):
    result = f'{first_name[0]}.{last_name[0]}.'
    print(result)

